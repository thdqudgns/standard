package standard.chapter04;

public class FlowEx16 {
	public static void main(String[] args) {
		
		//반복문으로 별찍기 5x5
		
		for(int i=1;i<=5;i++) {
			for(int j=1;j<=5;j++) {
				System.out.print("*");
			}
			System.out.println();
		}
		
		/*
		 * *****
		 * *****
		 * *****
		 * *****
		 * *****
		 */
		
	}//main

}//class
