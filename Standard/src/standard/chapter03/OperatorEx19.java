package standard.chapter03;

public class OperatorEx19 {
	public static void main(String args[]) {
		
		//나머지 연산자 %
		
		int x = 10;
		int y = 8;
		
		System.out.printf("%d을 %d로 나누면, %n", x, y);	//10을 8로 나누면, 
		System.out.printf("몫은 %d이고, 나머지는 %d입니다.%n", x/y, x%y);	//몫은 1이고, 나머지는 2입니다.
		
	} //main method

} //class
