package standard.chapter02;

public class SpecialCharEx {
	public static void main(String[] args) {
		//특별한 문자
		System.out.println('\'');	//'
		System.out.println("abc\t123\b456"); //abc	123456
		System.out.println('\n'); //개행
		System.out.println("\"Hello\""); //역슬래쉬를 이용해 큰 따옴표 출력
		System.out.println("c;\\"); //역슬래쉬로 역슬래쉬 출력
	}

}
